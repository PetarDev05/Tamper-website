export function starting() {
  let defaultValue = '';
  startingValues.forEach((value) => {
    const HTML = `
      <div class="menu-item">
        <div class="name-price">
          <p class="name">${value.name}</p>
          <p class="price">${value.price} RSD</p>
        </div>
        <img src="${value.image}" alt="" class="item-image">
      </div>
    `;

    defaultValue += HTML;

    document.querySelector('.js-products-holder').innerHTML = defaultValue;
    document.querySelector('.js-menu-item-name').innerHTML = 'Doručak sa jajima'
  });
}

export function getButton(buttonId) {
  let matching;
  buttons.forEach((button) => {
    
    if (buttonId === button.id) {
      matching = button;
    }
  });

  const name = matching.name;
  return name;
}

export function getRightProducts(buttonType) {
  let newProducts = [];
  products.forEach((product) => {
    if (buttonType === product.type) {
      newProducts.push(product);
    }
  });

  return newProducts;
}